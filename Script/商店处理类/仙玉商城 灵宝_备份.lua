    常规提示(id,"#Y这样的商品并不存在")
    return
  else
    local 名称=仙玉商城[大分类][类型][序号][1]
    local 仙玉=仙玉商城[大分类][类型][序号][4]
    -- 已删除神兽购买限制代码
