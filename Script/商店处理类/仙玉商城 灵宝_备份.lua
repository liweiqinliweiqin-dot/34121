   elseif 大分类 == "点卡商城"  then
     if 共享货币[玩家数据[id].账号]:扣除点卡(需求金钱,id,"商城购买") then
        self:仙玉商城商品处理(id,名称,数量)
        常规提示(id,format("#Y你花费了#R%s#Y点卡购买了#R%s",需求金钱,名称))
      end
  elseif 大分类 == "积分商城"  then
