function 系统处理类:抽奖中奖(id)
  local 玩家抽奖幸运值 = 0
  if id and 玩家数据[id] and 玩家数据[id].角色.数据.抽奖幸运值 then
    玩家抽奖幸运值 = 玩家数据[id].角色.数据.抽奖幸运值
  end

  local 必给奖励次数和物品 = {
    {次数=300, 物品名称="灵兜兜"},
    {次数=600, 物品名称="称号碎片"},
    {次数=1000, 物品名称="强化升级丹"},
    {次数=1999, 物品名称="锦衣升级卡"}
  }


  for _, v in ipairs(必给奖励次数和物品) do
    if 玩家抽奖幸运值 == v.次数 then

      if id and 玩家数据[id] then
        玩家数据[id].道具:自定义给予道具(id, v.物品名称, 1, 0)
        常规提示(id, "#Y/恭喜你获得了必给奖励: " .. v.物品名称 .. " 1#W/个")
        玩家数据[id].角色.数据.抽奖幸运值 = 玩家数据[id].角色.数据.抽奖幸运值 + 1
        return 0
      end


      if 自定义数据.抽奖配置 and #自定义数据.抽奖配置 > 0 then
        local 找到物品 = false
        for i=1,#自定义数据.抽奖配置 do
          if 自定义数据.抽奖配置[i].名称 and 自定义数据.抽奖配置[i].名称 == v.物品名称 then
            print("找到指定物品，索引:", i)
            找到物品 = true
            return i
          end
        end

        if not 找到物品 then
          print("警告: 未找到指定物品:", v.物品名称, "返回第一个物品")
          return 1
        end
      else
        print("警告: 自定义数据.抽奖配置不存在或为空")
      end
    end
  end

  if 玩家抽奖幸运值 >= 1999 and 玩家数据[id] then
    玩家数据[id].角色.数据.抽奖幸运值 = 0
    print("幸运值已重置为0")
  else
    玩家数据[id].角色.数据.抽奖幸运值 = 玩家数据[id].角色.数据.抽奖幸运值 + 1
  end

  local 中奖编号 = 0
  local 获得物品={}
  local 可以获得={}
  local 计算概率 ={}
  for i=1,#自定义数据.抽奖配置 do
    计算概率[i]={概率=自定义数据.抽奖配置[i].概率}
  end
  计算概率=删除重复(计算概率)
  table.sort(计算概率,  function(a,b) return a.概率>b.概率  end )
  local 获得概率 = 取随机数(1,计算概率[1].概率)
  for i=1,#自定义数据.抽奖配置 do
    if 自定义数据.抽奖配置[i].概率>0 and 获得概率<=自定义数据.抽奖配置[i].概率 then
      获得物品[#获得物品+1]={物品=自定义数据.抽奖配置[i],编号=i}
    end
    if 自定义数据.抽奖配置[i].概率>0 then
      可以获得[#可以获得+1] ={物品=自定义数据.抽奖配置[i],编号=i}
    end
  end
  获得物品=删除重复(获得物品)
  可以获得=删除重复(可以获得)
  if 获得物品~=nil then
    local 取编号=取随机数(1,#获得物品)
    if 获得物品[取编号]~=nil and 获得物品[取编号].物品~=nil and 获得物品[取编号].编号~=nil then
      中奖编号 = 获得物品[取编号].编号
    end
  end
  local 抽中物品名称 = nil
  if 中奖编号 > 0 and 自定义数据.抽奖配置[中奖编号] then
    抽中物品名称 = 自定义数据.抽奖配置[中奖编号].名称
  elseif 可以获得[1] then
    local 可以编号=可以获得[取随机数(1,#可以获得)].编号
    if 自定义数据.抽奖配置[可以编号] then
      抽中物品名称 = 自定义数据.抽奖配置[可以编号].名称
    end
    中奖编号 = 可以编号
  end

  if id and 玩家数据[id] and 抽中物品名称 then
    for _, v in ipairs(必给奖励次数和物品) do
      if v.次数 == 1999 and v.物品名称 == 抽中物品名称 then
        玩家数据[id].角色.数据.抽奖幸运值 = 0
        常规提示(id, "#Y/恭喜抽中稀有奖励，抽奖幸运值已重置！#W/")
        break
      end
    end
  end

  return 中奖编号
