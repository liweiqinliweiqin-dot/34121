          玩家数据[id].角色:添加银子(发送信息.银子,"月卡",1)
    end
    if 发送信息.抓鬼>0 then
      玩家数据[id].角色.数据.自动抓鬼 = 玩家数据[id].角色.数据.自动抓鬼 + 发送信息.抓鬼
