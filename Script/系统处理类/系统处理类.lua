function 系统处理类:剑会停止匹配处理(内容)
  local id=内容.数字id
  local 模式 = 内容.模式
  if 模式=="单人匹配" then
    for i=#剑会天下.单人,1,-1 do
      if 剑会天下.单人[i].id == id then
        table.remove(剑会天下.单人,i)
        break
      end
    end
