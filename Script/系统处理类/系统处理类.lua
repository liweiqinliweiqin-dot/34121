    --   共享货币[玩家数据[id].账号]:添加点卡(发送信息.点卡,id,"月卡领取")
    -- end
    if 发送信息.物品数量>0 then
      for i=1,发送信息.物品数量 do
        local 物品名称 = 发送信息.物品[i].名称
