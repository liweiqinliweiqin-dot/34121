      if string.find(r,v[1]) then
        return true
      end
    end
  else
    for k,v in pairs(自定义数据.判断敏感字) do
      if string.find(r,v[1]) then
        return string.gsub(r,v[1],v[2])
      end
    end
    return r
  end
end
