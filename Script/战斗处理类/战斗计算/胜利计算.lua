          for i,v in ipairs(临时属性) do
              if 玩家数据[id].角色.数据[v] then
                  首席争霸[玩家门派][v]=玩家数据[id].角色.数据[v]
              end
          end
          for i,v in ipairs(灵饰战斗属性) do
              if 玩家数据[id].角色.数据[v] then
                  首席争霸[玩家门派][v]=玩家数据[id].角色.数据[v]
