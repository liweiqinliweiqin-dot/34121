                  id组[#id组+1]=v.id
                  玩家数据[v.id].道具:自动回收(v.id)
            end
        end
        if 胜利id~=0 then
              if not 玩家数据[self.进入玩家id] then print("进入战斗id玩家为空"..self.进入玩家id) return end
              local 执行删除=function(地图,任务)
                    if 地图 then
                        删除单位(self.任务id)
                    end
                    if 任务 then
                        删除任务(self.任务id)
                    end
              end
              self.玩家胜利=true
              self:剧情奖励处理(self.进入玩家id)--110002到110015 100255到100261
              self:副本奖励处理(id组)
