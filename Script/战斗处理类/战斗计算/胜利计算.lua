          end
          玩家数据[id].角色:取消任务(任务id)
          if 降妖伏魔[id]==nil then
              降妖伏魔[id]=1
          end
          降妖伏魔[id]=降妖伏魔[id]+1
          if 取随机数(1,100)<=10 then
