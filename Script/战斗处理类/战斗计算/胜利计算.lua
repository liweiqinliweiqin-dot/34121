                首席争霸[玩家门派][v]=nil
          end
          for i,v in ipairs(灵饰战斗属性) do
                首席争霸[玩家门派][v]=nil
          end
          local 初始数据 ={等级=150,气血=20000,魔法=20000,愤怒=150,命中=3000,伤害=4500,防御=1500,法伤=4300,法防=1300,速度=1000,躲避=100}
          for k,v in pairs(初始数据) do
                首席争霸[玩家门派][k]=v
          end
